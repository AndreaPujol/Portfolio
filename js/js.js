// Funci√≥n para extraer ID de YouTube de diferentes formatos de URL
function extractYouTubeId(url) {
    const regExp = /^.*(youtu.be\/|v\/|u\/\w\/|embed\/|watch\?v=|&v=)([^#&?]*).*/;
    const match = url.match(regExp);
    return (match && match[2].length === 11) ? match[2] : null;
}

// Funci√≥n para reproducir videos de YouTube
function playYouTubeVideo(element, youtubeId, title) {
    const videoContainer = element.parentElement.parentElement;
    const escapedTitle = title.replace(/"/g, '&quot;').replace(/'/g, '&#39;');
    
    videoContainer.innerHTML = `
        <iframe 
            width="100%" 
            height="100%" 
            src="https://www.youtube.com/embed/${youtubeId}?autoplay=1&rel=0&modestbranding=1&playsinline=1" 
            title="${escapedTitle}" 
            frameborder="0" 
            allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" 
            allowfullscreen 
            class="w-full h-full">
        </iframe>
    `;
}
