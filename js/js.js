// Mejorar la navegación móvil
function enhanceMobileNavigation() {
    const mobileMenuBtn = document.getElementById('mobile-menu-btn');
    const mobileMenu = document.getElementById('mobile-menu');
    
    if (!mobileMenuBtn || !mobileMenu) {
        return;
    }
    
    // Función para toggle del menú
    function toggleMenu() {
        const isHidden = mobileMenu.classList.contains('hidden');
        
        if (isHidden) {
            mobileMenu.classList.remove('hidden');
            mobileMenu.style.animation = 'slideDown 0.3s ease-out forwards';
        } else {
            mobileMenu.style.animation = 'slideUp 0.3s ease-out forwards';
            setTimeout(() => {
                mobileMenu.classList.add('hidden');
                mobileMenu.style.animation = '';
            }, 250);
        }
    }
    
    // Event listeners para el botón
    mobileMenuBtn.addEventListener('click', function(e) {
        e.preventDefault();
        e.stopPropagation();
        toggleMenu();
    });
    
    mobileMenuBtn.addEventListener('touchend', function(e) {
        e.preventDefault();
        e.stopPropagation();
        toggleMenu();
    });
    
    // Cerrar menú al hacer clic en enlaces
    const menuLinks = mobileMenu.querySelectorAll('a');
    menuLinks.forEach(link => {
        link.addEventListener('click', function() {
            mobileMenu.style.animation = 'slideUp 0.3s ease-out forwards';
            setTimeout(() => {
                mobileMenu.classList.add('hidden');
                mobileMenu.style.animation = '';
            }, 250);
        });
    });
    
    // Cerrar menú al hacer clic fuera
    document.addEventListener('click', function(e) {
        if (!mobileMenu.classList.contains('hidden') && 
            !mobileMenu.contains(e.target) && 
            !mobileMenuBtn.contains(e.target)) {
            mobileMenu.style.animation = 'slideUp 0.3s ease-out forwards';
            setTimeout(() => {
                mobileMenu.classList.add('hidden');
                mobileMenu.style.animation = '';
            }, 250);
        }
    });
    
    // Agregar estilos de animación si no existen
    if (!document.getElementById('mobile-menu-animations')) {
        const animationStyles = document.createElement('style');
        animationStyles.id = 'mobile-menu-animations';
        animationStyles.textContent = `
            @keyframes slideDown {
                from { 
                    opacity: 0; 
                    transform: translateY(-10px);
                    visibility: visible;
                }
                to { 
                    opacity: 1; 
                    transform: translateY(0);
                    visibility: visible;
                }
            }
            
            @keyframes slideUp {
                from { 
                    opacity: 1; 
                    transform: translateY(0);
                    visibility: visible;
                }
                to { 
                    opacity: 0; 
                    transform: translateY(-10px);
                    visibility: hidden;
                }
            }
        `;
        document.head.appendChild(animationStyles);
    }
}

